package com.example.proyectofinciclo.models;

public class partido {

    private String fecha, gloc, gvis, loc, vis, estadio,has, min;

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getGloc() {
        return gloc;
    }

    public void setGloc(String gloc) {
        this.gloc = gloc;
    }

    public String getGvis() {
        return gvis;
    }

    public void setGvis(String gvis) {
        this.gvis = gvis;
    }

    public String getLoc() {
        return loc;
    }

    public void setLoc(String loc) {
        this.loc = loc;
    }

    public String getVis() {
        return vis;
    }

    public void setVis(String vis) {
        this.vis = vis;
    }

    public String getEstadio() {
        return estadio;
    }

    public void setEstadio(String estadio) {
        this.estadio = estadio;
    }

    public String getHas() {
        return has;
    }

    public void setHas(String has) {
        this.has = has;
    }

    public String getMin() {
        return min;
    }

    public void setMin(String min) { this.min = min; }
}